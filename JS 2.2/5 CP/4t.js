function kingSayd(string) {
    if (string.startsWith("Король сказал:")) {
      console.log(string);
    } else {
      console.log("Король сказал: " + string);
    }
  }
  
  // Пример использования:
  kingSayd("сегодня хорошая погода"); // Король сказал: сегодня хорошая погода
  kingSayd("Король сказал: завтра будет дождь"); // Король сказал: завтра будет дождь


//   В этом решении используется метод startsWith() для проверки, начинается ли строка с подстроки "Король сказал:". Если да, то строка выводится в консоль без изменений. Если нет, то к строке добавляется подстрока "Король сказал:" перед ее выводом в консоль.